package target

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

func init() {
	Register(&AIDLCTarget{})
}

// AIDLCTarget exports to AWS AI-DLC Workflows format.
// The target generates input documents for the AIDLC Inception phase:
// - vision-document.md (from MRD/Press/PRD)
// - technical-environment.md (from TRD/IRD/context)
// - imported-requirements.md (combined requirements)
type AIDLCTarget struct{}

// Name returns the target name.
func (t *AIDLCTarget) Name() string {
	return "aidlc"
}

// Description returns a description of the target.
func (t *AIDLCTarget) Description() string {
	return "AWS AI-DLC Workflows format (vision-document.md, technical-environment.md, imported-requirements.md)"
}

// Capabilities returns what this target supports.
func (t *AIDLCTarget) Capabilities() Capabilities {
	return Capabilities{
		SequentialTasks:   true,
		ParallelExecution: false,
		MultiAgent:        true, // AIDLC supports multi-agent via specialist phases
		Verification:      true,
		DependencyGraph:   true,
	}
}

// Validate checks if the spec can be exported to this target.
func (t *AIDLCTarget) Validate(spec string) error {
	if spec == "" {
		return fmt.Errorf("spec content is empty")
	}
	return nil
}

// Export exports the spec to AIDLC format.
// The config.Options may include:
// - "mrd": MRD content for vision document
// - "prd": PRD content for vision document
// - "press": Press release content for vision document
// - "trd": TRD content for technical environment
// - "ird": IRD content for technical environment
// - "context": Codebase context for technical environment
func (t *AIDLCTarget) Export(spec string, config ExportConfig) (*ExportResult, error) {
	if err := t.Validate(spec); err != nil {
		return nil, err
	}

	// Determine output directory
	outputDir := config.OutputDir
	if outputDir == "" {
		outputDir = ".aidlc"
	}

	// Create output directory
	if err := os.MkdirAll(outputDir, 0755); err != nil {
		return nil, fmt.Errorf("creating output directory: %w", err)
	}

	var files []string

	// Generate vision-document.md
	visionDoc := t.generateVisionDocument(spec, config)
	visionPath := filepath.Join(outputDir, "vision-document.md")
	if err := os.WriteFile(visionPath, []byte(visionDoc), 0600); err != nil {
		return nil, fmt.Errorf("writing vision-document.md: %w", err)
	}
	files = append(files, visionPath)

	// Generate technical-environment.md
	techEnvDoc := t.generateTechnicalEnvironment(config)
	techEnvPath := filepath.Join(outputDir, "technical-environment.md")
	if err := os.WriteFile(techEnvPath, []byte(techEnvDoc), 0600); err != nil {
		return nil, fmt.Errorf("writing technical-environment.md: %w", err)
	}
	files = append(files, techEnvPath)

	// Generate imported-requirements.md from reconciled spec
	importedReqs := t.generateImportedRequirements(spec, config.ProjectName)
	importedPath := filepath.Join(outputDir, "imported-requirements.md")
	if err := os.WriteFile(importedPath, []byte(importedReqs), 0600); err != nil {
		return nil, fmt.Errorf("writing imported-requirements.md: %w", err)
	}
	files = append(files, importedPath)

	return &ExportResult{
		Target:    t.Name(),
		OutputDir: outputDir,
		Files:     files,
		Success:   true,
		Message:   fmt.Sprintf("Exported to %s for AIDLC Inception phase", outputDir),
	}, nil
}

// generateVisionDocument creates the AIDLC Vision Document from VisionSpec artifacts.
// Maps: MRD -> Problem/Opportunity, Press -> Vision Statement, PRD -> Goals/Success Metrics.
func (t *AIDLCTarget) generateVisionDocument(spec string, config ExportConfig) string {
	var sb strings.Builder

	sb.WriteString("# Vision Document\n\n")
	sb.WriteString("*Generated by VisionSpec for AWS AI-DLC Workflows*\n\n")

	// Problem/Opportunity section (from MRD)
	sb.WriteString("## Problem/Opportunity\n\n")
	if mrd, ok := config.Options["mrd"].(string); ok && mrd != "" {
		sb.WriteString(extractSection(mrd, "Problem", "Opportunity", "Market Need"))
	} else {
		sb.WriteString("*See imported-requirements.md for problem definition.*\n")
	}
	sb.WriteString("\n")

	// Vision Statement (from Press Release)
	sb.WriteString("## Vision Statement\n\n")
	if press, ok := config.Options["press"].(string); ok && press != "" {
		sb.WriteString(extractSection(press, "Summary", "Headline", "Lead"))
	} else {
		sb.WriteString("*See imported-requirements.md for vision.*\n")
	}
	sb.WriteString("\n")

	// Goals and Success Metrics (from PRD)
	sb.WriteString("## Goals and Success Metrics\n\n")
	if prd, ok := config.Options["prd"].(string); ok && prd != "" {
		sb.WriteString(extractSection(prd, "Goals", "Metrics", "Success Criteria", "Acceptance Criteria"))
	} else {
		sb.WriteString("*See imported-requirements.md for goals and metrics.*\n")
	}
	sb.WriteString("\n")

	// Target Users (from MRD/PRD)
	sb.WriteString("## Target Users\n\n")
	if mrd, ok := config.Options["mrd"].(string); ok && mrd != "" {
		sb.WriteString(extractSection(mrd, "Target", "Users", "Audience", "Persona"))
	}
	sb.WriteString("\n")

	// Key Features (from spec.md)
	sb.WriteString("## Key Features\n\n")
	if spec != "" {
		sb.WriteString(extractSection(spec, "Features", "Functional Requirements", "User Stories"))
	}
	sb.WriteString("\n")

	sb.WriteString("---\n\n")
	sb.WriteString("*Use with AIDLC Inception phase: \"Using AI-DLC, develop this feature based on the vision document.\"*\n")

	return sb.String()
}

// generateTechnicalEnvironment creates the AIDLC Technical Environment Document.
// Maps: TRD -> Architecture/APIs, IRD -> Infrastructure, Context -> Existing Systems.
func (t *AIDLCTarget) generateTechnicalEnvironment(config ExportConfig) string {
	var sb strings.Builder

	sb.WriteString("# Technical Environment Document\n\n")
	sb.WriteString("*Generated by VisionSpec for AWS AI-DLC Workflows*\n\n")

	// Existing Systems (from context)
	sb.WriteString("## Existing Systems\n\n")
	if ctx, ok := config.Options["context"].(string); ok && ctx != "" {
		sb.WriteString(ctx)
	} else {
		sb.WriteString("*No codebase context available. AI-DLC will analyze the repository directly.*\n")
	}
	sb.WriteString("\n\n")

	// Architecture (from TRD)
	sb.WriteString("## Architecture\n\n")
	if trd, ok := config.Options["trd"].(string); ok && trd != "" {
		sb.WriteString(extractSection(trd, "Architecture", "System Design", "Components", "Overview"))
	} else {
		sb.WriteString("*See imported-requirements.md for technical architecture.*\n")
	}
	sb.WriteString("\n")

	// APIs and Interfaces (from TRD)
	sb.WriteString("## APIs and Interfaces\n\n")
	if trd, ok := config.Options["trd"].(string); ok && trd != "" {
		sb.WriteString(extractSection(trd, "API", "Interface", "Contract", "Endpoints"))
	} else {
		sb.WriteString("*To be defined during AIDLC Construction phase.*\n")
	}
	sb.WriteString("\n")

	// Infrastructure (from IRD)
	sb.WriteString("## Infrastructure\n\n")
	if ird, ok := config.Options["ird"].(string); ok && ird != "" {
		sb.WriteString(extractSection(ird, "Infrastructure", "Deployment", "Environment", "Cloud"))
	} else {
		sb.WriteString("*To be defined during AIDLC Construction phase.*\n")
	}
	sb.WriteString("\n")

	// Constraints and Dependencies (from TRD/IRD)
	sb.WriteString("## Constraints and Dependencies\n\n")
	if trd, ok := config.Options["trd"].(string); ok && trd != "" {
		sb.WriteString(extractSection(trd, "Constraints", "Dependencies", "Limitations", "Non-Functional"))
	}
	sb.WriteString("\n")

	sb.WriteString("---\n\n")
	sb.WriteString("*AI-DLC will use this document to understand the technical landscape.*\n")

	return sb.String()
}

// generateImportedRequirements creates the combined requirements document.
// This is the reconciled spec.md formatted for AIDLC consumption.
func (t *AIDLCTarget) generateImportedRequirements(spec string, projectName string) string {
	var sb strings.Builder

	sb.WriteString("# Imported Requirements: ")
	sb.WriteString(projectName)
	sb.WriteString("\n\n")

	sb.WriteString("*Generated by VisionSpec for AWS AI-DLC Workflows*\n\n")

	sb.WriteString("## Source\n\n")
	sb.WriteString("This document contains requirements reconciled from:\n\n")
	sb.WriteString("- Market Requirements Document (MRD)\n")
	sb.WriteString("- Product Requirements Document (PRD)\n")
	sb.WriteString("- User Experience Design (UXD)\n")
	sb.WriteString("- Technical Requirements Document (TRD)\n")
	sb.WriteString("\n")

	sb.WriteString("## Requirements\n\n")
	if spec != "" {
		sb.WriteString(spec)
	} else {
		sb.WriteString("*No reconciled requirements available. Run `visionspec reconcile` first.*\n")
	}
	sb.WriteString("\n")

	sb.WriteString("---\n\n")
	sb.WriteString("## AI-DLC Integration\n\n")
	sb.WriteString("To use these requirements with AI-DLC:\n\n")
	sb.WriteString("1. Copy the `.aidlc/` directory to your project\n")
	sb.WriteString("2. Start the Inception phase: \"Using AI-DLC, develop this feature based on imported-requirements.md\"\n")
	sb.WriteString("3. AI-DLC will drive requirements refinement through its approval gates\n")

	return sb.String()
}

// extractSection attempts to extract relevant content from a document.
// It looks for headings containing any of the search terms and extracts that section.
func extractSection(content string, searchTerms ...string) string {
	lines := strings.Split(content, "\n")
	var result []string
	capturing := false
	captureDepth := 0

	for _, line := range lines {
		// Check if this is a heading
		if strings.HasPrefix(line, "#") {
			// Count heading depth
			depth := 0
			for _, c := range line {
				if c == '#' {
					depth++
				} else {
					break
				}
			}

			// If we're capturing and hit a same-or-higher level heading, stop
			if capturing && depth <= captureDepth {
				break
			}

			// Check if heading matches any search term
			lineLower := strings.ToLower(line)
			for _, term := range searchTerms {
				if strings.Contains(lineLower, strings.ToLower(term)) {
					capturing = true
					captureDepth = depth
					// Don't include the heading itself, just the content
					break
				}
			}
			continue
		}

		if capturing {
			result = append(result, line)
		}
	}

	extracted := strings.TrimSpace(strings.Join(result, "\n"))
	if extracted == "" {
		return "*Content not found in source document.*\n"
	}
	return extracted + "\n"
}
