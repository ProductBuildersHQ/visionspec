// Package mkdocs provides MkDocs integration for visionspec.
package mkdocs

import (
	"encoding/json"
	"fmt"
	"io"
	"os"
	"path/filepath"
	"strings"

	"github.com/ProductBuildersHQ/visionspec/pkg/eval"
)

// RenderEvalOptions configures evaluation rendering.
type RenderEvalOptions struct {
	// IncludeEvidence includes evidence snippets in findings.
	IncludeEvidence bool
	// IncludeJudgeMetadata includes information about the LLM judge.
	IncludeJudgeMetadata bool
}

// RenderEvalToMarkdown converts an evaluation result to markdown.
func RenderEvalToMarkdown(w io.Writer, result *eval.Result, opts RenderEvalOptions) error {
	// Header
	fmt.Fprintf(w, "# %s Evaluation\n\n", strings.ToUpper(string(result.SpecType)))

	// Summary box
	statusEmoji := "❌"
	statusText := "FAIL"
	if result.Passed {
		statusEmoji = "✅"
		statusText = "PASS"
	}

	fmt.Fprintf(w, "| Metric | Value |\n")
	fmt.Fprintf(w, "|--------|-------|\n")
	fmt.Fprintf(w, "| **Status** | %s %s |\n", statusEmoji, statusText)
	fmt.Fprintf(w, "| **Score** | %.1f/10 |\n", result.Score)
	fmt.Fprintf(w, "| **Evaluated** | %s |\n", result.Timestamp.Format("2006-01-02 15:04:05"))
	fmt.Fprintf(w, "\n")

	// Summary
	if result.Summary != "" {
		fmt.Fprintf(w, "## Summary\n\n")
		fmt.Fprintf(w, "%s\n\n", result.Summary)
	}

	// Category Scores
	fmt.Fprintf(w, "## Category Scores\n\n")
	fmt.Fprintf(w, "| Category | Score | Weight | Status |\n")
	fmt.Fprintf(w, "|----------|-------|--------|--------|\n")

	for _, cat := range result.Categories {
		catStatus := "❌"
		if cat.Score >= 7.0 {
			catStatus = "✅"
		} else if cat.Score >= 5.0 {
			catStatus = "⚠️"
		}

		fmt.Fprintf(w, "| %s | %.1f | %.0f%% | %s |\n",
			cat.Name, cat.Score, cat.Weight*100, catStatus)
	}
	fmt.Fprintf(w, "\n")

	// Category Details
	fmt.Fprintf(w, "## Category Details\n\n")
	for _, cat := range result.Categories {
		fmt.Fprintf(w, "### %s\n\n", cat.Name)
		fmt.Fprintf(w, "**Score:** %.1f/10\n\n", cat.Score)
		if cat.Explanation != "" {
			fmt.Fprintf(w, "%s\n\n", cat.Explanation)
		}
	}

	// Findings
	if len(result.Findings) > 0 {
		fmt.Fprintf(w, "## Findings\n\n")

		// Group by severity
		bySeverity := map[string][]eval.Finding{
			"critical": {},
			"high":     {},
			"medium":   {},
			"low":      {},
			"info":     {},
		}
		for _, f := range result.Findings {
			bySeverity[f.Severity] = append(bySeverity[f.Severity], f)
		}

		// Summary counts
		fmt.Fprintf(w, "| Severity | Count |\n")
		fmt.Fprintf(w, "|----------|-------|\n")
		for _, sev := range []string{"critical", "high", "medium", "low", "info"} {
			if len(bySeverity[sev]) > 0 {
				emoji := severityEmoji(sev)
				fmt.Fprintf(w, "| %s %s | %d |\n", emoji, strings.Title(sev), len(bySeverity[sev]))
			}
		}
		fmt.Fprintf(w, "\n")

		// Detailed findings
		for _, sev := range []string{"critical", "high", "medium", "low", "info"} {
			findings := bySeverity[sev]
			if len(findings) == 0 {
				continue
			}

			emoji := severityEmoji(sev)
			fmt.Fprintf(w, "### %s %s\n\n", emoji, strings.Title(sev))

			for i, f := range findings {
				fmt.Fprintf(w, "#### %d. %s\n\n", i+1, f.Title)
				fmt.Fprintf(w, "**Category:** %s\n\n", f.Category)
				fmt.Fprintf(w, "%s\n\n", f.Description)

				if f.Recommendation != "" {
					fmt.Fprintf(w, "**Recommendation:** %s\n\n", f.Recommendation)
				}

				if opts.IncludeEvidence && f.Evidence != "" {
					fmt.Fprintf(w, "<details>\n<summary>Evidence</summary>\n\n")
					fmt.Fprintf(w, "```\n%s\n```\n\n", f.Evidence)
					fmt.Fprintf(w, "</details>\n\n")
				}
			}
		}
	}

	// Judge metadata
	if opts.IncludeJudgeMetadata {
		fmt.Fprintf(w, "## Judge Information\n\n")
		fmt.Fprintf(w, "| Property | Value |\n")
		fmt.Fprintf(w, "|----------|-------|\n")
		fmt.Fprintf(w, "| Model | %s |\n", result.Judge.Model)
		fmt.Fprintf(w, "| Provider | %s |\n", result.Judge.Provider)
		fmt.Fprintf(w, "| Temperature | %.2f |\n", result.Judge.Temperature)
		fmt.Fprintf(w, "| Tokens | %d |\n", result.Judge.Tokens)
		fmt.Fprintf(w, "\n")
	}

	fmt.Fprintf(w, "---\n")
	fmt.Fprintf(w, "*Generated by VisionSpec*\n")

	return nil
}

// severityEmoji returns an emoji for the severity level.
func severityEmoji(severity string) string {
	switch severity {
	case "critical":
		return "🔴"
	case "high":
		return "🟠"
	case "medium":
		return "🟡"
	case "low":
		return "🔵"
	case "info":
		return "ℹ️"
	default:
		return "⚪"
	}
}

// RenderEvalFile reads an eval JSON file and renders it to markdown.
func RenderEvalFile(evalPath string, outputPath string, opts RenderEvalOptions) error {
	// Read eval JSON
	data, err := os.ReadFile(evalPath)
	if err != nil {
		return fmt.Errorf("reading eval file: %w", err)
	}

	var result eval.Result
	if err := json.Unmarshal(data, &result); err != nil {
		return fmt.Errorf("parsing eval JSON: %w", err)
	}

	// Create output file
	f, err := os.Create(outputPath)
	if err != nil {
		return fmt.Errorf("creating output file: %w", err)
	}
	defer f.Close()

	return RenderEvalToMarkdown(f, &result, opts)
}

// RenderAllEvals renders all eval JSON files in a project to markdown.
func RenderAllEvals(projectPath string, opts RenderEvalOptions) (int, error) {
	evalDir := filepath.Join(projectPath, "eval")

	entries, err := os.ReadDir(evalDir)
	if err != nil {
		if os.IsNotExist(err) {
			return 0, nil // No eval directory
		}
		return 0, fmt.Errorf("reading eval directory: %w", err)
	}

	count := 0
	for _, entry := range entries {
		if entry.IsDir() {
			continue
		}
		if !strings.HasSuffix(entry.Name(), ".eval.json") {
			continue
		}

		inputPath := filepath.Join(evalDir, entry.Name())
		outputName := strings.TrimSuffix(entry.Name(), ".json") + ".md"
		outputPath := filepath.Join(evalDir, outputName)

		if err := RenderEvalFile(inputPath, outputPath, opts); err != nil {
			return count, fmt.Errorf("rendering %s: %w", entry.Name(), err)
		}
		count++
	}

	return count, nil
}
